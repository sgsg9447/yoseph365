/** id로 단일 게시글 조회 */
export function getInquiryById(id: number): Inquiry | undefined {
  return INQUIRY_POSTS.find((p) => p.id === id);
}

/** 목록 순서(최신순)에서 이전글(index+1) / 다음글(index-1) 반환 */
export function getAdjacentInquiries(id: number): {
  prev: Inquiry | null;
  next: Inquiry | null;
} {
  const idx = INQUIRY_POSTS.findIndex((p) => p.id === id);
  if (idx < 0) return { prev: null, next: null };
  return {
    prev: INQUIRY_POSTS[idx + 1] ?? null,
    next: INQUIRY_POSTS[idx - 1] ?? null,
  };
}
